package Aula05;

public class Aula05Tipos {

	public static void main(String[] args) {
		
		
		float preco = 10.99f;
		float preco2 = (float) 24.99;
		
		System.out.println("O valor do produto é: " + preco2);
		
		char letra = 'S';
		
		System.out.println("A letra escolhida é: 6" + letra);
		
		boolean ligado = true;
		
		System.out.println(ligado);
		
		boolean desligado = (boolean) false;
		System.out.println(desligado);
		
		double precoDouble = 10.99;
		
		System.out.println(precoDouble);
		
		
	}

}
