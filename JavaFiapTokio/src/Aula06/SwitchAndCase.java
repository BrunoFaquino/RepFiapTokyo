package Aula06;

public class SwitchAndCase {

	public static void main(String[] args) {
		
		int escolha = 1;
		
		switch (escolha) {
		case 0:
			System.out.println("Sair do programa");
			break;
			
		case 1:
			System.out.println("Entrou no programa");
					break;
					
		default:
			System.out.println("Error");
		}

	}

}
